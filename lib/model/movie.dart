class Movie {
  final int id;
  final String title;
  final String category;
  final String year;
  final double rating;
  final String description;
  final String imagePath;

  Movie({
    required this.id,
    required this.title,
    required this.category,
    required this.year,
    required this.rating,
    required this.description,
    required this.imagePath,
  });
}
